const cardsByNumber = new Map<number, LotteryCard>(
  lotteryCards.map((card) => [card.numero, card])
)

export function getLotteryCard(cardNumber: number | string) {
  const parsed = typeof cardNumber === 'string' ? Number(cardNumber) : cardNumber
  if (Number.isNaN(parsed)) {
    return undefined
  }
  return cardsByNumber.get(parsed)
}
